#include <iostream>
using namespace std;

int main()
{
    // pengulangan menaik
    cout << "PENGULANGAN MENAIK" << endl;
    for (int C=0; C<10; C++)
    {
        cout << C+1 << endl;

    }
    cout << "\n";
    // pengulangan munurun
    cout << "PENGULANGAN MENURUN" << endl;
    for (int D=0; D<10; D--)
    {
        cout << D << endl;
    }
    return 0;
}
